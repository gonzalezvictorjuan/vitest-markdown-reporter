type TestWithPartialContext = Omit<Test, "context"> & Partial<Pick<Test, "context">>;

const test = <T extends TestWithPartialContext>(t: T): Test => {
  const context = {
    meta: t,
    onTestFailed: (fn) => {
      if (t.result) fn(t.result);
    },
    expect,
  } as TestContext;
  t.context = context;
  return t as Test;
};

